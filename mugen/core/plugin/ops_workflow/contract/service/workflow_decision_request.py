"""Provides a service contract for WorkflowDecisionRequestDE services."""

__all__ = ["IWorkflowDecisionRequestService"]

import uuid
from abc import ABC, abstractmethod
from typing import Any, Mapping

from mugen.core.contract.gateway.storage.rdbms.crud_base import ICrudService
from mugen.core.plugin.acp.contract.api.validation import IValidationBase
from mugen.core.plugin.ops_workflow.domain import WorkflowDecisionRequestDE


class IWorkflowDecisionRequestService(
    ICrudService[WorkflowDecisionRequestDE],
    ABC,
):
    """A service contract for workflow decision request services."""

    @abstractmethod
    async def action_open(
        self,
        *,
        tenant_id: uuid.UUID,
        where: Mapping[str, Any],
        auth_user_id: uuid.UUID,
        data: IValidationBase,
    ) -> tuple[dict[str, Any], int]:
        """Open a decision request."""

    @abstractmethod
    async def action_resolve(
        self,
        *,
        tenant_id: uuid.UUID,
        entity_id: uuid.UUID,
        where: Mapping[str, Any],
        auth_user_id: uuid.UUID,
        data: IValidationBase,
    ) -> tuple[dict[str, Any], int]:
        """Resolve a decision request."""

    @abstractmethod
    async def action_cancel(
        self,
        *,
        tenant_id: uuid.UUID,
        entity_id: uuid.UUID,
        where: Mapping[str, Any],
        auth_user_id: uuid.UUID,
        data: IValidationBase,
    ) -> tuple[dict[str, Any], int]:
        """Cancel a decision request."""

    @abstractmethod
    async def action_expire_overdue(
        self,
        *,
        tenant_id: uuid.UUID,
        where: Mapping[str, Any],
        auth_user_id: uuid.UUID,
        data: IValidationBase,
    ) -> tuple[dict[str, Any], int]:
        """Expire overdue decision requests."""
