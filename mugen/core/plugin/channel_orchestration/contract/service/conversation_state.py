"""Provides a service contract for ConversationStateDE-related services."""

__all__ = ["IConversationStateService"]

import uuid
from abc import ABC, abstractmethod
from typing import Any, Mapping

from mugen.core.contract.gateway.storage.rdbms.crud_base import ICrudService
from mugen.core.plugin.acp.contract.api.validation import IValidationBase
from mugen.core.plugin.channel_orchestration.domain import ConversationStateDE


class IConversationStateService(ICrudService[ConversationStateDE], ABC):
    """A service contract for ConversationStateDE-related services."""

    @abstractmethod
    async def action_evaluate_intake(
        self,
        *,
        tenant_id: uuid.UUID,
        entity_id: uuid.UUID,
        where: Mapping[str, Any],
        auth_user_id: uuid.UUID,
        data: IValidationBase,
    ) -> tuple[dict[str, Any], int]:
        """Evaluate intake inputs and select the best matching intake rule."""

    @abstractmethod
    async def action_route(
        self,
        *,
        tenant_id: uuid.UUID,
        entity_id: uuid.UUID,
        where: Mapping[str, Any],
        auth_user_id: uuid.UUID,
        data: IValidationBase,
    ) -> tuple[dict[str, Any], int]:
        """Resolve routing from routing rules and fallback policy."""

    @abstractmethod
    async def action_escalate(
        self,
        *,
        tenant_id: uuid.UUID,
        entity_id: uuid.UUID,
        where: Mapping[str, Any],
        auth_user_id: uuid.UUID,
        data: IValidationBase,
    ) -> tuple[dict[str, Any], int]:
        """Escalate a conversation and record the decision timeline event."""

    @abstractmethod
    async def action_apply_throttle(
        self,
        *,
        tenant_id: uuid.UUID,
        entity_id: uuid.UUID,
        where: Mapping[str, Any],
        auth_user_id: uuid.UUID,
        data: IValidationBase,
    ) -> tuple[dict[str, Any], int]:
        """Apply throttle policy and optionally block the sender."""

    @abstractmethod
    async def action_set_fallback(
        self,
        *,
        tenant_id: uuid.UUID,
        entity_id: uuid.UUID,
        where: Mapping[str, Any],
        auth_user_id: uuid.UUID,
        data: IValidationBase,
    ) -> tuple[dict[str, Any], int]:
        """Set fallback mode/target for the conversation."""
